package com.example.demo.domain;

public class Dto01 {

	// dto 라는 이름은..
	// data transfer object 이다.

	private String name;
	private int age;
	
	

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
